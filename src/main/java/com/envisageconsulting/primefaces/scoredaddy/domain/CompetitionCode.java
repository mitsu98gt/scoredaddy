package com.envisageconsulting.primefaces.scoredaddy.domain;

public class CompetitionCode {

    private String competitionCodeId;
    private String code;
    private String description;

    public String getCompetitionCodeId() {
        return competitionCodeId;
    }

    public void setCompetitionCodeId(String competitionCodeId) {
        this.competitionCodeId = competitionCodeId;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
